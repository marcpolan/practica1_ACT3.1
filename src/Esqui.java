class Esqui {

	String noms;
	int alt;
	int remuntador;
	int quant_pistes;
	int forfait;

	Esqui() {

	}

	Esqui(String noms, int alt, int remuntador, int quant_pistes, int forfait) {
		this.noms = noms;
		this.alt = alt;
		this.remuntador = remuntador;
		this.quant_pistes = quant_pistes;
		this.forfait = forfait;
	}

}
